// ğŸ§© Convert summary to TXT
// ==========================================================
export const convertSummaryToTXT = (summary, languageCode = "en") => {
  try {
    const lines = summary.split("\n").filter((line) => line.trim().length > 0);
    let txtContent = `ğŸ¬ Video Summary (${languageCode.toUpperCase()})\n`;
    txtContent += `====================================\n\n`;

    lines.forEach((line, index) => {
      const cleanLine = line.replace(/^[â€¢\-*\d.]\s*/, "").trim();
      if (cleanLine.length > 0) {
        txtContent += `${index + 1}. ${cleanLine}\n`;
      }
    });

    txtContent += `\n====================================\n`;
    txtContent += `âœ… Generated by AI Video Summarizer`;

    return txtContent;
  } catch (err) {
    console.error("âŒ TXT conversion error:", err.message);
    return `Error creating TXT file: ${err.message}`;
  }
};

